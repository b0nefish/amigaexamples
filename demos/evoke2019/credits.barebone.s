;;; Draw the sinus scroll -- but fast...
;;; Input
;;; A0=pointer to the Bitplaneinfo structure
;;; A1=pointer to the source area (this can even be in FAST RAM)
;;; D0.w=row number for sinus scroller.
_full_sinus_scroll:
	;; A6=$DFF000
	;; A5=bitplaneinfo
	;; A4=sinus data ptr
	;; A3=source data ptr
	;; A2=row address array (ptr)
	;; D7=X coordinate (or running index)
	;; D6=row number of sinus center
reg$:	REG	d6-d7/a2-a6
	movem.l	reg$,-(sp)
	moveq	#0,d7		; First column, set X to zero.
	move.w	d0,d6		; Store row number of center
	lea.l	$DFF000,a6
	move.l	a0,a5		; Save bitplaneinfo into A5
	lea.l	_long_sinusdat,a4
	lea.l	(a1),a3
	lea.l	bplinfo_row_addresses_0(a5),a2 ; Put row address pointer into A1
	tst.w	bplinfo_bplidx(a5) ; Are we in the 0th or 1st bitplane?
	beq.s	l1$
	lea.l	bplinfo_row_addresses_1(a5),a2 ; Advance to the secondary plane.
l1$:	
	;; Calculate the sinus
	move.w	d7,d1		; D1=X
	lsl.w	#1,d1		; D1*=3
	add.w	d7,d1
	add.w	phase$(pc),d1	; Add phase
	and.w	#4096-1,d1	; Length of table, DANGER!
	move.b	(a4,d1.w),d1	; sin()
	ext.w	d1		; Extend the sinus (in byte) to a word.
	add.w	d6,d1		; Center the sinus, now D1=Y
	lsl.w	#2,d1		; Get index into the row table.
	move.l	(a2,d1.w),a1	; Get address into A1
	move.w	d7,d1		; D1=x
	lsr.w	#3,d1		; Divide X-position by 8 to get the byte
	and.w	#$FFFE,d1	; Must be even.
	add.w	d1,a1		; Adjust plane pointer
	;; Wait for the blitter?
l2$:	btst	#6,dmaconr(a6)
	bne.s	l2$
	move.l	a1,bltcpt(a6)
	move.l	a1,bltdpt(a6)
	move.w	(a3)+,bltbdat(a6)	; Store the pattern.
	move.w	#$8000,bltadat(a6)	; Initialise line-draw bit
	move.w	d7,d0		; D0=X
	ror.w	#4,d0		; Four right is equivalent to 12 left.
	and.w	#$F000,d0	; If the lower bits are removed.
	or.w	#$0bca,d0	; Channels and use an OR.
	move.w	d0,bltcon0(a6)
	move.w	#15*64+66,bltsize(a6) ; Start the blit.
	addq	#1,d7
	cmp.w	#319,d7
	ble.s	l1$
	add.w	#17,phase$
	movem.l	(sp)+,reg$
	rts
phase$:	dc.w	0


;;; Copy the credits copperlist to the target area. The very first thing the copper list does it to set up the credits-scroller bitplane pointers. Therefore if the pointer needs to be update this can be easily done later on.
	;; Input
	;; A0=target
	;; A1=address of bitplane1
	;; A2=address of bitplane2 (aka background)
	;; Output
	;; D0=End address of generated copperlist.
;;; ULONG copy_credits_copperlist(__reg("a0") void *target, __reg("a1") void *credits_bitplane, __reg("a2") void *logo_bitplane);
_copy_credits_copperlist:
	move.l	a1,d0		; Move credits bitplane address to d0
	swap	d0		; We need the upper 16 bits later.
	move.w	#bplpt+4,(a0)+	; Bitplane pointer
	move.w	d0,(a0)+	; Upper 16 bits
	move.w	#bplpt+6,(a0)+	; Lower 16 bits of bitplane poiner.
	move.w	a1,(a0)+	; The bits.
	;; a2: logo image, has two interleaved bitplanes
	move.l	a2,d0		; Move logo bitplane address to d0.
	swap	d0		; We need the upper 16 bits later.
	move.w	#bplpt+0,(a0)+	; Bitplane pointer
	move.w	d0,(a0)+	; Upper 16 bits
	move.w	#bplpt+2,(a0)+	; Lower 16 bits of bitplane poiner.
	move.w	a2,(a0)+	; The bits.
	lea.l	320/8(a2),a2	; Second bitplane
	move.l	a2,d0
	swap	d0
	move.w	#bplpt+8,(a0)+
	move.w	d0,(a0)+
	move.w	#bplpt+10,(a0)+
	move.w	a2,(a0)+
	lea.l	credits_copperlist,a1 ; Get the rest of the copperlist.
	move.w	#(credits_copperlist_end-credits_copperlist)/2-1,d0 ; Words
l1$:	move.w	(a1)+,(a0)+
	dbf	d0,l1$
	move.l	a0,d0		; Move current address into d0.
	rts

;;; void copy_bicolor_image(UBYTE *bitplane);
;;; This function will copy the image into a target bitplane. It will just uncompress the IFF/ILBM data.
_copy_bicolor_image:
	;; A5: target area
	move.l	4(sp),d0
	movem.l	d2-d7/a2-a7,-(sp)
	move.l	d0,a5		; Area to copy image to.
	lea.l	credits_image,a0
	move.l	#"BODY",d0
	jsr	find_iff_chunk
	tst.l	d0
	bmi	out$
	;; A0=body data
	;; D0=body size (e.g. compressed data bytes)
	move.l	a5,a1		; Get target area.
	jsr	uncompress_body_interleaved
out$:	movem.l	(sp)+,d2-d7/a2-a7
	rts


